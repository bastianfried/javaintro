package aufgaben;

public class AdditionsRechner {

	public static void main(String[] args) {
		for(int zahlA = 1; zahlA<=100; zahlA=zahlA + 1) {
		for(int zahlB = 1; zahlB<=100; zahlB=zahlB + 1) {	
			System.out.println(zahlA + "+" + zahlB + "=" + (zahlA + zahlB));
		
		
		}
		}
		

	}

}
